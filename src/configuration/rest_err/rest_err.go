package resterr_test

import "net/http"

type RestErr struct {
	Message string   `json:"message"`
	Err     string   `json:"error"`
	Code    int      `json:"code"`
	Causes  []Causes `json:"causes"` // Array que conterá os campos que estão preenchidos de forma incorreta
}

type Causes struct {
	Field   string `json:"field"`
	Message string `json:"message"`
}

//Criação de uma interface error para satisfazer as bibliotecas que recebem error por parametros
// ou retornam error por parametros
func (r *RestErr) Error() string {
	return r.Message
}

// construtor do objeto de erros
func NewRestErr(message, err string, code int, causes []Causes) *RestErr {
	return &RestErr{
		Message: message,
		Err:     err,
		Code:    code,
		Causes:  causes,
	}
}

